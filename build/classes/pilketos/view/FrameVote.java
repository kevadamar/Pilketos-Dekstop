    private void getData(){
        try {
            Statement stat = connection.createStatement();
            String sql = "SELECT u.username,c.user_id,c.visi_misi,c.point,c.status FROM "
                    + "user u INNER JOIN candidat c ON c.user_id = u.id WHERE c.status = 'new'";
            ResultSet res = stat.executeQuery(sql);
            int i = 1;
            while(res.next ()) {
                if(i == 1){
                    txtnama1.setText(res.getString("username"));
                    txtvisimisi1.setText(res.getString("visi_misi"));
                } else {
                    txtnama2.setText(res.getString("username"));
                    txtvisimisi2.setText(res.getString("visi_misi"));
                }
                i+=1;
            }
        } catch(SQLException err) {
            err.printStackTrace();
            System.out.println(err);
        }
    }
    
    private void updateVoted(String nama, String point, String userLogin){
        PreparedStatement statement = null;
        String sql = "UPDATE candidat SET point=?,useriddone=? WHERE username=?";
        try {
            statement = connection.prepareStatement(sql, Statement.RETURN_GENERATED_KEYS);
            statement.setString(1, point);
            statement.setString(2, userLogin);
            statement.setString(3, nama);
            statement.executeUpdate();
        } catch (SQLException ex) {
            ex.printStackTrace();
        } finally {
            try {
                statement.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }
    
